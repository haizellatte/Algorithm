function solution(number) {
    let answer = 0;
    
    for (let i = 0; i < number.length; i++) {
      const a = number[i];
      for (let j = i+1; j <= i; j++) {
        const b = number[j];
            for (let k = j+1; k <= j; k++) {
              const c = number[k];
              if (a + b + c === 0) answer++;
            }
        }
    }
  
  answer;
}

